package nur.prog3.arboles.ui;

public class NodoGrafico {

    private int x;
    private int y;

    public NodoGrafico(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
